package L5;

public class Demo1 {

    public void show(int x) {
        System.out.println("Int: " + x);
    }

    public void show(String x) {
        System.out.println("String: " + x);
    }

    public void show(byte x) {
        System.out.println("byte: " + x);
    }

    public static final void show(char x) {
        System.out.println("byte: " + x);
    }
}
